    {
        $adminRoleId = DB::table('roles')->where('name', 'admin')->value('id');
        User::updateOrCreate(
            ['email' => 'admin@example.com'],
            [
                'name' => 'Admin User',
                'password' => Hash::make('password'),
                'role_id' => $adminRoleId,
            ]
        );
    }
}
